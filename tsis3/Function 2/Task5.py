def cat_av_IMDB(movies,cat_name):
    lst=[]
    for i in range(len(movies)-1):
        if movies[i]['category'] == cat_name:
            lst.append(movies[i])
        
    

    n = len(lst)
    av_imdb = 0
    for i in lst:
        av_imdb = av_imdb+i['imdb']
    av_imdb = av_imdb/n
    
    return av_imdb

        
